    private void addbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addbtnMouseClicked
       
        if(claasName.getText().equals("") && claasName.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Error! Enter Class name and Section");
            return;
        }
        if (claasName.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Error! Enter Class name ");
            return;
       
        }
        if(Section.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Error!Enter Section");
            return;
      
        }
        
        classes classes = new classes(claasName.getText(), Section.getText());
            classes.addClass();
            Section.setText("");
            claasName.setText("");
            idnotf1.setText("");
           
       this.classes.ClassesRecord(jTable1);
                
    }//GEN-LAST:event_addbtnMouseClicked

